#include <iostream>
#include <bits/stdc++.h>
using namespace std;
typedef long long int ll;
int main()
{
    string s;
    cin >> s;
    ll ans = 0;
    ll n = s.size();
    for (ll i = 0; i < n; i++)
    {
        if ((s[i] - 48) % 3 == 0)
        {
            ans++;
        }
        else if (i < n - 1)
        {
            if (((s[i] - 48) % 3 == 1 && (s[i + 1] - 48) % 3 == 2) || ((s[i + 1] - 48) % 3 == 1 && (s[i] - 48) % 3 == 2))
            {
                ans++;
                i++;
            }
            else if (i < n - 2)
            {
                if (((s[i] - 48) % 3 == 2 && (s[i + 1] - 48) % 3 == 2 && (s[i + 2] - 48) % 3 == 2) || ((s[i] - 48) % 3 == 1 && (s[i + 1] - 48) % 3 == 1 && (s[i + 2] - 48) % 3 == 1))
                {
                    ans++;
                    i += 2;
                }
            }
        }
        else if (i < n - 2)
        {
            if (((s[i] - 48) % 3 == 2 && (s[i + 1] - 48) % 3 == 2 && (s[i + 2] - 48) % 3 == 2) || ((s[i] - 48) % 3 == 1 && (s[i + 1] - 48) % 3 == 1 && (s[i + 2] - 48) % 3 == 1))
            {
                ans++;
                i += 2;
            }
        }
    }
    ll ans1 = 0;
    for (ll i = n - 1; i >= 0; i--)
    {
        if ((s[i] - 48) % 3 == 0)
        {
            ans1++;
        }
        else if (i >= 1)
        {
            if (((s[i] - 48) % 3 == 1 && (s[i - 1] - 48) % 3 == 2) || ((s[i - 1] - 48) % 3 == 1 && (s[i] - 48) % 3 == 2))
            {
                ans1++;
                i--;
            }
            else if (i >= 2)
            {
                if (((s[i] - 48) % 3 == 2 && (s[i - 1] - 48) % 3 == 2 && (s[i - 2] - 48) % 3 == 2) || ((s[i] - 48) % 3 == 1 && (s[i - 1] - 48) % 3 == 1 && (s[i - 2] - 48) % 3 == 1))
                {
                    ans1++;
                    i -= 2;
                }
            }
        }
        else if (i >= 2)
        {
            if (((s[i] - 48) % 3 == 2 && (s[i - 1] - 48) % 3 == 2 && (s[i - 2] - 48) % 3 == 2) || ((s[i] - 48) % 3 == 1 && (s[i - 1] - 48) % 3 == 1 && (s[i - 2] - 48) % 3 == 1))
            {
                ans1++;
                i -= 2;
            }
        }
    }
    cout << max(ans, ans1) << "\n";
}